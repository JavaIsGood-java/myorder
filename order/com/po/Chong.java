package com.po;

public class Chong {
	private int id;
	private String username;
	private double sal;
	private double fsal;
	private double asal;
	private String date;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public double getSal() {
		return sal;
	}
	public void setSal(double sal) {
		this.sal = sal;
	}
	public double getFsal() {
		return fsal;
	}
	public void setFsal(double fsal) {
		this.fsal = fsal;
	}
	public double getAsal() {
		return asal;
	}
	public void setAsal(double asal) {
		this.asal = asal;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
}
